export function asciiArt(str: string) {
  // Split the string into an array of characters
  const characters = str.split('');

  // Map the characters to their ASCII art representation
  const ascii = characters.map((char: string) => {
    if (char === " ") {
      return char;
    }
    const c: string = char.toUpperCase();
    if (Object(asciiCharacters).hasOwnProperty(c)) {
      return asciiCharacters[c as keyof typeof asciiCharacters];
    }
    throw new Error("Character not found");
  });

  // Join the ASCII art characters into a single string
  return ascii.join('');
}
